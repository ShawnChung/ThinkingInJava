package operators;

public class Exponents {
	public static void main(String[] args) {
		float floatExp = 47e-44f;
		System.out.println("floatExp : " + floatExp);
		double doubleExp = 47e47;
		System.out.println("doubleExp : " + doubleExp);
		double d = 4700000000000000000000000000000000000000.0;
		System.out.println("d : " + d);
	}
}
